package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {

        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';

        int intC = (int) c;
        int intH = (int) h;
        int intA = (int) a;
        int intR = (int) r;

        System.out.println(intC);
        System.out.println(intH);
        System.out.println(intA);
        System.out.println(intR);
    }
}
